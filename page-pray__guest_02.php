      こちらは2013年、六本木のギャラリーle bainにて、アルテマイスター主催〈内田繁 祈りのかたち　デザイン仏壇『白虹（はっこう）』発表〉と題した展覧会の会場で催された、インテリアデザイナー内田繁氏と、展覧会のコーディネーターであり、〈新しい祈りのかたち〉のプロデュースを手掛けている、山田節子氏によるオープニングトークをまとめたものです。<br>
      過去・今・未来を見つめながら、地域や民族、固有の文化の意義や価値を改めて見直し、今日の生活がより良くあるためのデザインとは如何にあるべきかを常に問い続け、表徴・表現され続けた内田氏。<br>
      その氏にこれからの祈りのかたちとしてデザインしていただいた、仏壇「白虹」や「厨子」のデザインに込められた、深い思いが伺える貴重な言葉が残されていました。<br>
      人間社会優先型の驕りの世紀に、自然や人間社会の秩序が崩れていく日々の中にあって〈人は如何に生きるべきか〉が問われる今、改めて祈りある日々のためにと、ご紹介させていただくことといたしました。<br>
      （編集協力：山田節子）
