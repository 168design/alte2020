    <p class="single-body__text-a">
      古来よりこの国は、四季の移り変わりに心を寄せ、自然を重んじ、その自然に畏敬の念を持ちながら、恵みに感謝し暮らしてきました。海外の文化も巧みに学び、取り入れ、独自の解釈で文化として様々に育み続けてきました。<br>
私は、それらの固有の生活文化を少しでも未来に繋いでいくことが出来たらと、日々学び、歩み続けて行けたらと願っています。<br>
「山川草木悉皆成仏」という言葉を忘れずに、人間もまた、自然の一部であることを念頭に、作る人と使う人を繋ぎ、心と心が通いあうことで、生きる喜びが立ち上がり続けることを願っております。<br>
ネット上で、どこでも簡単にものが買える時代ですが、モノの一つひとつが、どのような素材でどのように作られているのか。そしてどれほど人の手をかけて生まれてきているのか。簡単便利で、生き急ぐ暮らしより、作る人・使う人・売る人の心が満たされ、地球にも人にも優しい、未来を模索する責任を常に念頭にと思っています。
    </p>

    <p class="single-body__text-q">
      ― なるほど。松屋で続けられていることと、当社での取り組みが、一本の線で繋がっていることがお話しを伺う中で見えてきました。<br>
ここで、「祈り」の話に戻り、山田さんにお持ち頂いたご自宅のお写真をもとに、日々の暮らしのお話しを伺いたいと思います。
    </p>

