    <p class="single-body__text-a">
      何と発売初日、50パック程の玉子は一時間ほどで売り切れたのです。その後、いち早く食品売り場に自然食の店として「正直村」が誕生しました。<br>
また、「茶の葉」の誕生は、私のアシスタントの家に「急須がない」、という話が発端でした。ご飯とお茶は日本の食文化の根幹をなすものですが、当時は、雑誌でもコーヒーや紅茶ばかりにスポットが当たる時代でした。その一方で何事によらず、大量生産、大量消費に主眼が置かれ、茶畑に大量の化学肥料が投入され、根が弱り日本茶本来の美味しさが失われ始めた時代でした。<br>
「加速する日本茶の衰退は日本文化の衰退にもつながる」と思い、お茶の淹れ方が見える、味わえる場を、銀座の一等地にある百貨店の中にと松屋に懇願したことがきっかけでした。<br>
その他に、毎年暮れに開催されている「日本のかたち」という催事や、日本文化の伝統美を回顧し未来に繋ぐ催事「華麗なる装いの美」「桜」「王朝の遊び」「日本のいろ」の展示会も松屋の方々はもちろん、多くの心ある方々のお力添えをいただき、松屋という場の力あり、実現できたことでありました。<br>
松屋というところは、モノやヒト・コトが集まる所で、博識で、次の時代を作っていかれる方が出入りされるところです。様々な出会いを重ねさせていただき、時の流れの中で、モノ・コトを学び、形にして行ける、かけがえなき場であると感じています。
    </p>

