    <p class="single-body__text-a">
      そうですね。これは伊藤さんの「仏足（ぶっそく）」という作品です。自宅の庭、椿の木の下に置いています。朝、窓を開け庭を見ると、そこに仏が立っておいでのような気がして、自分の心が清められ、背筋が伸びるような気がいたします。
    </p>

    <p class="single-body__text-q">
      ― ご自身の暮らしの中に「祈り」が自然と息づいているように感じます。
    </p>

    <p class="single-body__text-a">
      温暖化のニュースを目に耳にする度に、どうしたら孫の世代に未来を残せるだろうかと、日々真剣に考えさせられます。過去に学び、未来にバトンを託そうとする……例えば厨子というものは、そうした自分の心を見つめる箱でもあると思うのです。内田繁さんと厨子を手がけたときも、例えば玄関に置けるようなものも欲しいとも頼みました。たとえ、一人住まいの方であっても、出かけるときに「行ってきます」、帰ってきた時「ただいま」と声をかけられるような、親に、先祖に、大切な人に見守ってもらえている、そんな心の拠り所が必要な時代ではないかと。厨子はそうした日々の中で、願いや祈りの心を納め、受け止めてもらえる、心の箱でもあるのではないでしょうか。
    </p>

    <p class="single-body__text-q">
      ― 忙しい世の中で埋没してしまう、自身と向き合う、心豊かな時間が必要と感じます。日々の暮らしで意識していることはありますか？
    </p>

