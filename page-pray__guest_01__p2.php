    <p class="single-body__text-a">
      こちらは、陶芸家の<a href="#" class="overlay-btn" data-trg="ito">伊藤慶二さん（※3）</a>が作られた膳に、これもまた伊藤さんが作られた仏を置いたものです。私がまだ20代の終わり頃、青山のグリーンギャラリーで、この白い膳だけが並び、その存在感に圧倒された展示会がありました。その場では買えず、どうしても忘れがたく、後から問い合わせたところ、「残っているよ、見に来てご覧」ということになり、ご自宅のある美濃まで出かけ、買い求め、それからの長いお付き合いとなりました。伊藤さんはものごとの道理を、深く考えられ、表現され、作品に魂を感じさせられる稀有な作り手のお一人です。<br>
孫が小さな頃、この家にやってくると、部屋から部屋へ走り回って遊ぶのですが、不思議なことに、こちらの手を合わせて祷る像の前に来ると、教えてもいませんのにスッと手を合わせて、チョコンと頭を下げるのです。ビックリしました。祈りの所作が自然と子供の感受性に響き、知らず知らずのうちに真意が伝わるのでしょうか。モノの力は言葉なくして伝わること、思い知らされたことでした。
    </p>

    <p class="single-body__text-q">
      ― 私も、子どもの行動にハッとさせられることが多々あります。こちらも伊藤さんの作品ですか。
    </p>

