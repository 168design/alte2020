    <p class="single-body__text-q">
      ― 山田さんがものづくりの世界に携わるようになられたきっかけは、やはり、松屋銀座のお仕事がおおきいのでしょうか。
    </p>

    <p class="single-body__text-a">
      松屋銀座さんとは1970年代からずっと、半世紀も仕事をさせていただいております。デザインの松屋と云われ、学生時代から憧れの場でもありました。<br>
初めての大型企画催事が高度成長前夜の1977年「日本人の食器展」でした。それは日常の食卓の基本、飯椀・汁椀・箸など、全国の心ある作り手の方々が作るモノを、それぞれ100点を目標に集め、300坪以上ある会場での、当時では珍しいカタログ的な展示販売会でした。<br>
その後、テーマを変えながら80年代半まで、年一回の大仕事でありました。
    </p>

