    <p class="single-body__text-q">
      ― 「日本人の食器展」はどのような展示会だったのですか。
    </p>

    <p class="single-body__text-a">
      当時は、東京オリンピック、大阪万博が終わり、欧米文化優先で海外旅行、洋風惣菜などが注目され、生活よりファッションへ、作るより買う時代へと、大量生産大量消費が美徳の世の中へ変貌の始まりでした。<br>
そのような時代の中で、世界でも独特な日本の食文化や器文化の美意識を忘れずに、未来化を提案するために、心ある作り手の方々に、日常的に使う、よい器を手がけて頂ければと考えたのです。飯椀、汁椀、箸、小皿……大皿大鉢等々、それらをつくっていただくために、全国の産地を訪ね歩くようになっていきました。<br>
当時はあちこちに、人格的にも表現や技術でも優れた親方のような方がいらっしゃいました。夜になると若き作り手達が親方の家の囲炉裏の周りに集まり、「自分たちは自然の命、材料を使わせてもらっているのだから、いい加減なものはつくるなよ」と技術のことはもちろん、材料の特性や時代性、つくったものがどのように経年変化するかなど、作り手としての人生論を織り交ぜながら語り伝えられていました。私もその片隅に座らせていただき、耳を傾け学ばせていただきました。
    </p>

    <p class="single-body__text-q">
      ― 展示会を企画する中で、ものが生まれる場・人との関係性を深めていったのですね。その他にも様々なお仕事をされています。
    </p>

    <p class="single-body__text-a">
      1981年の自然食の「正直村」という、自然食の先駆けショップの誕生も、その翌年本物の日本茶「茶の葉」という茶席の発端も、いずれも、私の日常の生活の中での些細な発見がコトの始まりでした。<br>
息子の3歳の誕生日を椿山荘で祝った時、玉子の美味しさにお驚き、お伺いすると、「当店では、那須の牧場で飼われている平飼いの玉子を使っております」とのこと。「本物の玉子の味を伝えたい」と、その話を聞き届けて下さり、「坊や。これが、本当の玉子だよ。」の全面広告で、当時の玉子の３倍の値段もする平飼いの玉子を販売に漕ぎ着けました。
    </p>

    <figure class="single-body__image">
      <picture class="be--spw-full be--pcw-600">
