    <p class="single-body__text-q">
      ― 余材を活かす取り組みとして始まった当社オリジナルブランド「人と木」も意識改革の一つですよね。
    </p>

    <p class="single-body__text-a">
      そうですね。仏壇・仏具を作る材はどれも立派ですが、形を抜いた残りの材は、ストーブで燃やされていました。ならばと、余材を活かす取り組みとして「人と木」というブランドを立ち上げ、技術あるベテラン職人の力も借りながら、若い人たちが自由なものづくりを考える場を設けました。<br>
時代を読み、感じたことを形にする中で、必要とされるものを見極める、目と心を育むことが目的でした。自分の日々を見直しながら、これから必要とされることを考え開発する目線が、少しずつ養われ、本業にも活かされ始めていると感じられるようになりました。
    </p>

