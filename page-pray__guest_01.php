    </p>

    <p class="single-body__text-a">
      その新しい厨子の製造を手掛けたのは、伝統的な仏壇を作り続けてきたアルテマイスターのベテラン職人たちですから、そう簡単には運びませんでした。例えば、デザイナーは角をシャープに出したいと、図面に注意書きを書いたとしても、職人の手が習慣的に動いて角を落としている。「角を立てた天板を作る」というのは、やってはいけない仕事だった等々、大変でした。<br>
技術者集団と、内田繁氏とのそれぞれのプライドの対決は、日に日に融和され始め、その後、様々な習慣を越えたあとの仕事の確かさ、熱心さ、美しさはデザイナー内田繁を「やるじゃないか、いいね」と驚かせ、喜ばれました。職人軍団もまた、褒められて、未知なる世界を「かたち化」していくことに次々と挑戦をはじめ、基礎技術あってこそ頼もしき力となりました。<br>
最初に話しましたように、伝統産業が抱える問題は、デザインという言葉に踊らされ、技術がありながらもそれを現代に活かしきれないことにあります。心あるデザイナーとのものづくりは、伝統の技術を現代に活かす、大きな刺激を職人軍団に与える取り組みとなりました。
    </p>

    <p class="single-body__text-q">
      ― 厨子の開発と合わせ、当社社員の意識改革にも取り組まれました。
    </p>

    <p class="single-body__text-a">
      始めた当初、ベテラン職人と若い人たちとの間には、殆ど会話がない現場でした。年長者は、礼儀を知らない若者に教える気になれないと言い、若い人は、年のいった人にいろいろ言われたくない、という雰囲気でした。そこで、社員みんなが集える心地よい空間を設けてはと、社長にお願いをいたしました。<br>
2005年だったでしょうか。新たに作られた食堂のオープンは年の初めにと計画し、事前に若手社員を募り、オープニングパーティーとしての「新年会」を企画しました。まず、「正月とは何か」。正月のお飾り一つにも意味があり、正月に使う柳箸は、雪が積もっても雪折れしない柳の木の強さに倣い、強い体、強い心でと願う意味がある。この国の『精神文化のことわり』を心と体で学びながら、お節料理や正月飾りもすべて作り整えました。<br>
新たにつくられた食堂で、若い社員が準備のすべてを体験し、見事にもてなした時、長老格の人々が「やるじゃん！」と。その日は近年にない大雪の正月でありましたが、皆さん機嫌は上々。それがきっかけで壁が外れ笑顔が生まれ、雪解けの様に世代間の歩み寄りが始まったと感じています。<br>
会津魂でしょうか、「やらねばなりませぬ」の会津の藩政以来の心意気が、今に生きていると感じた事でもありました。
    </p>

