<article class="content single-body">
  <div class="inner">
    <p class="single-body__text-q">
      ― 仕事では大変お世話になっており、改めてお話しをお伺いするのは少し気恥ずかしさを感じますが、はじめに、当社との関係のはじまりから伺いたいと思います。
    </p>

    <p class="single-body__text-a">
      もう20年も前のことになりますが、それ以前からお付き合いのあった、会津若松にある漆問屋「坂本乙造商店」の坂本朝夫さんから、「会津の仏壇メーカーのショールームのディスプレイを見てもらえないでしょうか？」。と連絡がありまして、見るぐらいならと軽い気持ちで会津に出かけたのが始まりでした。<br>
そのショールームは、デザイナー<a href="#" class="overlay-btn" data-trg="mizutani" >水谷壮市氏（※1）</a>による、神秘的で斬新な見事な空間でしたが、そこに置かれていた仏壇とは全く似合わず、余りの違和感にその場に立ち尽くしてしまいました。
    </p>

    <figure class="single-body__image">
      <picture class="be--spw-full be--pcw-600">
        <img src="<?php echo get_template_directory_uri(); ?>/img/pray/guest_01/p1-showroom.jpg" alt="⽔⾕壮市⽒デザインによる当時のショールーム" />
      </picture>
      <figcaption>⽔⾕壮市⽒デザインによる当時のショールーム</figcaption>
