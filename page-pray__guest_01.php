    </figure>


    <p class="single-body__text-q">
      ― なるほど。ではなぜ、「仏壇」ではなく「厨子」だったのでしょうか。
    </p>

    <p class="single-body__text-a">
      仏壇は、江戸時代に幕府の人民統制として、檀家制度を定め、先祖・親・故人の位牌等を納め祀ると共に、「家」を単位とした、精神の拠り所としての形でもありました。<br>
しかし、第二次世界大戦後、家長制度が廃止され、個人の自由が重んじられる時代になりましたが、その分、家族の絆が薄れ、核家族・単身世帯が増え続けた昨今。テレビや新聞から流れてくる突発的な悲しいニュースに表れるように、社会の揺らぎ、家族の崩壊などで、人の心が失われ置き去りにされていく時代であればこそ。先祖や親を弔い、静かに手を合わせ、自分の心を取り戻し、日常の平安・希望を願う、時間や場が必要ではないかとの思いに至ったこと。<br>
さらに、暮らしの場から床の間が消え、座卓からテーブル・椅子へと生活空間が様変わりする中で、従来の祭壇などは、置き場を失い、手つかずのまま。<br>
ならば現代の住空間に似合い、現代の人それぞれの生き方に寄り添い、大切な『コト』『ココロ』を安置する、現代の祈りの箱「厨子」をとの思いに至りました。
    </p>

    <p class="single-body__text-q">
      ― そのような思いの中で、なぜ、最初の厨子のデザインをインテリアデザイナーの<a href="#" class="overlay-btn" data-trg="uchida" >内田繁氏（※2）</a>に依頼されたのでしょうか。
    </p>

    <p class="single-body__text-a">
      内田さんとは50年来のお付き合いでした。悲しいことに4年前に亡くなられてしまわれたのですが、最初にお願いする人は内田繁さんと決めていました。<br>
      それまで、多様な難題の仕事をご一緒する中で、海外文化に触発されながらも、日本文化の根源性を見失うことなく、「山川草木悉皆成仏（すべてのものに神宿り命あるものは死して再び自然に帰り神となる）」という、日本文化の根源的感覚を共有しているという信頼感が有ったからでした。<br>
      まず頭に浮かんだイメージは、内田さんが、1999年オランダで発表されたキャビネット『マトリックス』。そして、翌年ミラノで発表された木製棚『ホリゾンタル』。それを原型に、今までの仏壇や厨子の対局にあるような、重圧感や、威圧感から解放されながらも、清浄感のある『現代の祈りの箱』を仕立てたいという強い願いがあったからでした。
    </p>

