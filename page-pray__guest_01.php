      ― 様々な取り組みの集大成として、本社がある福島県の会津で、2011年から取引先の方々を招いて、展示会を開催し始めました。展示会はその年の新商品を発表する当社の一大イベントとなり、年々、来場いただく方が増えています。
    </p>

    <p class="single-body__text-a">
      当初は、営業さんを始め、「会津まで来てくれるかと」訝しがる人が多かったと思われます。しかも、初年度はあの3.11東日本大震災の年の6月でした。しかし、ありがたい事で、多くの方々がおいでくださり、私も感謝いたすばかりでありました。その様な中で、作る人と商う人が、直接出会える場を設けることで、作り手も心が奮い立ち、商う人も、使う人への確かなメッセージを伝えられる、絆を育む場となりました。<br>
IＴの時代が来ていますが、機械に委ねられる、遠隔地操作も便利ではありますが、人の心、人の手がどこかに置いていかれるようになってしまったら、本当のものづくりの歴史は続いていけるだろうかと案じられます。<br>
ましてや、人類存続の危機と言われるコロナ禍の今、改めて、自然の命をいただき、ものは生まれるという事を、今まで以上に心に深く刻む時であればこそ、人の暮らしに寄り添うものを、人の心が潤うものを作り続けていくことが大切な事ではないかと、願い続けています。<br>
そのためには、手間暇間が掛かっても、この会津の地にて、確かな人の確かな手によってものが生まれる処をご覧いただくことで、信頼感ある企業として、未来へ歩み続けて行って欲しいと思っています。
    </p>

