シンプルなフォルムに、ブルーやレッドなどの色使いはこれまでの厨子の概念をはるかに越えていますが、リビングルームのインテリアとしても違和感がなく、現代の暮らしに適う、象徴的な生活美・祈りのかたちが生まれました。さすが内田さん、内田さん無くして、未来への扉は開かなかったと思っています。
    </p>

