  <div class="inner">
    <p>
      アルテマイスター 東日本大震災10周年企画展「3.11鎮魂 -魂の宿る場-」展の開催を前に、陶芸家 伊藤慶二さんの岐阜県土岐市のご自宅にお伺いいたしました。<br>
※この記事は2020年11月に行ったインタビューの内容をまとめたものです。<br>
<br>
伊藤慶二さんは、ものごとの本質を問い続け、表現されるアーティストであり、「HIROSHIMA」や「足（そく）」・「面（つら）」など　一連の造形作品には氏の精神性が色濃く現され、生涯のテーマとして制作を続けられている、稀有な陶芸家です。<br>
祈りの文化を未来に繋ぐことを使命とする企業として、東日本大震災があった翌年の2012年に、当社ギャラリーにて《伊藤慶二「3.11鎮魂」展》を開催いたしました。<br>
破壊された自然や歴史的遺産、そして災害に見舞われた方々のご苦難など、多くの事々を心に刻み、忘れぬことこそが不可欠と、鎮魂〈生きとし生けるものの魂を鎮め、未来へと生きる〉をテーマとした展覧会でした。<br>
震災から10年の節目となる2021年、再び鎮魂展開催は不可欠と伊藤慶二氏に依頼し、鎮魂展の準備をいたしてまいりました。<br>
この日、前回を振り返ると共に、今回の作品への思いや、常なる創作活動の事など、展覧会のディレクターである<a href="#" class="overlay-btn" data-trg="yamada" >山田節子（※1）</a>さんと共にお話しを伺いいたしました。<br>
（聞き手：アルテマイスター編集部）
